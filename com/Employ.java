package com;
public  class Employ{
        private String name;
        private int exp;
        private double salary;
       
        public String getName() {
            return name;
        }
        public double getSalary() {
            return salary;
        }
        public void setSalary(double salary) {
            this.salary = salary;
        }
        public void setName(String name) {
            this.name = name;
        }
        public int getExp() {
            return exp;
        }
        public void setExp(int exp) {
            this.exp = exp;
        }
        
}